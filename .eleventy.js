const { minify: minifyHtml } = require("html-minifier-terser");
const CleanCSS = require("clean-css");
const { minify: minifyJs } = require("terser");
const fs = require("fs");
const path = require("path");

module.exports = function (eleventyConfig) {
  eleventyConfig.setNunjucksEnvironmentOptions({ trimBlocks: true, lstripBlocks: true });

  // ── Static asset passthrough ──────────────────────────────────────────────
  eleventyConfig.addPassthroughCopy({ "css":    "css"    });
  eleventyConfig.addPassthroughCopy({ "js":     "js"     });
  eleventyConfig.addPassthroughCopy({ "images": "images" });
  eleventyConfig.addPassthroughCopy({ "fonts":  "fonts"  });
  // data/posts.json is generated by src/data/posts.njk — no passthrough needed

  // Root files
  eleventyConfig.addPassthroughCopy({ "favicon.jpg":  "favicon.jpg"  });
  eleventyConfig.addPassthroughCopy({ "favicon.ico":  "favicon.ico"  });
  eleventyConfig.addPassthroughCopy({ "favicon.png":  "favicon.png"  });
  eleventyConfig.addPassthroughCopy({ "robots.txt":   "robots.txt"   });
  // sitemap.xml is generated by src/sitemap.njk — no passthrough needed
  eleventyConfig.addPassthroughCopy({ "googlec1e17be5846de750.html": "googlec1e17be5846de750.html" });

  // ── Filters ───────────────────────────────────────────────────────────────
  // Format any value (Date object or ISO string) as YYYY-MM-DD for sitemap
  eleventyConfig.addFilter("isoDate", (val) => {
    if (!val) return "";
    if (val instanceof Date) return val.toISOString().slice(0, 10);
    return String(val).slice(0, 10);
  });

  // ── Collections ───────────────────────────────────────────────────────────
  // Pages included in sitemap.xml (excludes 404, noindex pages, helper templates)
  eleventyConfig.addCollection("sitemap", (api) =>
    api.getAll().filter(p =>
      p.url &&
      p.data.robots !== "noindex" &&
      p.data.eleventyExcludeFromCollections !== true &&
      !p.url.endsWith("/data/posts.json")
    )
  );

  // ── HTML minification (every build) ──────────────────────────────────────
  eleventyConfig.addTransform("htmlmin", async (content, outputPath) => {
    if (outputPath && outputPath.endsWith(".html")) {
      return await minifyHtml(content, {
        removeComments:              true,
        collapseWhitespace:          true,
        removeAttributeQuotes:       true,
        collapseBooleanAttributes:   true,
        removeRedundantAttributes:   true,
        useShortDoctype:             true,
        minifyCSS:                   false,
        minifyJS:                    false,
      });
    }
    return content;
  });

  // ── CSS + JS minification (build mode only, not serve/watch) ─────────────
  eleventyConfig.on("eleventy.after", async ({ runMode }) => {
    if (runMode !== "build") return;

    // CSS
    const cssPath = path.join("_site", "css", "style.css");
    const cssInput = fs.readFileSync(cssPath, "utf8");
    const cssResult = new CleanCSS({ level: 2 }).minify(cssInput);
    fs.writeFileSync(cssPath, cssResult.styles);
    const cssSaving = Math.round((1 - cssResult.stats.minifiedSize / cssResult.stats.originalSize) * 100);
    console.log(`[minify] CSS  ${cssResult.stats.originalSize} → ${cssResult.stats.minifiedSize} bytes (−${cssSaving}%)`);

    // JS
    const jsPath = path.join("_site", "js", "main.js");
    const jsInput = fs.readFileSync(jsPath, "utf8");
    const jsResult = await minifyJs(jsInput, { compress: true, mangle: true });
    fs.writeFileSync(jsPath, jsResult.code);
    const jsSaving = Math.round((1 - jsResult.code.length / jsInput.length) * 100);
    console.log(`[minify] JS   ${jsInput.length} → ${jsResult.code.length} bytes (−${jsSaving}%)`);
  });

  return {
    dir: {
      input:    "src",
      output:   "_site",
      includes: "_includes",
      data:     "_data",
    },
    htmlTemplateEngine: "njk",
    markdownTemplateEngine: "njk",
  };
};
